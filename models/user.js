class User {
    constructor(id, name, email, mobileNumber, age, gender, password, cart, favorite ){
        this.id = id;
        this.name = name;
        this.email = email;
        this.mobileNumber = mobileNumber;
        this.age = age;
        this.gender = gender; 
        this.password = password;
        this.cart = cart;
        this.favorite = favorite;
    }
}